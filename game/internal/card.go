func ShuffleCards() []int {
	i := len(cardsBase) - 1
	for i >= 1 {
		j := rand.Int63n(int64(i + 1))
		cardsBase[i], cardsBase[j] = cardsBase[j], cardsBase[i]
		i--
	}

	return cardsBase
}

